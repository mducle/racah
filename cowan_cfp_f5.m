% Calculates the coefficient of fractional parentage for l=3, after the method of Racah IV.

if nargin==3 && ischar(U) && ischar(v)
%  if ~ischar(n)
%    nc = ['f' char(double(n)+48)];
%    np = ['f' char(double(n-1)+48)];
%  else
%    nc = n;
%    np = ['f' char(double(n(2)-1))];
%    n = double(n(2)-48);
%  end
%  stc = racah_states(nc,U);  % Child state
%  stp = racah_states(np,v);  % Parent state
%  U  = stc{4}; v  = stc{3}; S  = stc{1}; L  = stc{2};
%  Up = stp{4}; vp = stp{3}; Sp = stp{1}; Lp = stp{2};
  if strcmp(n,'f5')
    stc = U;
    stp = v;
    [stf5,stidf5] = racah_states(5,3);
    [stf4,stidf4] = racah_states(4,3);
    istc = find(strcmp(U,stidf5));
    istp = find(strcmp(v,stidf4));
  else
    error('Sorry only f5 implemented so far.');
  end
  cfp = 0;
else
  [stf5,stidf5] = racah_states(5,3);
  [stf4,stidf4] = racah_states(4,3);
  for i = 1:length(stf5)
    if (U==stf5{i}{4}) & (v==stf5{i}{3}) & (S==stf5{i}{1}) & strcmp(L,stf5{i}{2})
      stc = stidf5{i};
      istc = i;
      break
    end;
  end
  for i = 1:length(stf4)
    if (Up==stf4{i}{4}) & (vp==stf4{i}{3}) & (Sp==stf4{i}{1}) & strcmp(Lp,stf4{i}{2})
      stp = stidf4{i};
      istp = i;
      break
    end
  end
  cfp = 0;
  if ~exist('stc') | ~exist('stp')
    return
  end
end

